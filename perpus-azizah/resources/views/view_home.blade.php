    <h3>Definisi perpustakaan menurut para ahli</h3>
    <p>
    <h4>Perpustakaan</h4>
        Menurut UNESCO (United Nations Educational, Scientific and Cultural Organization):
        Perpustakaan adalah "sebuah koleksi buku dan materi lainnya yang disusun dan diatur untuk memenuhi kebutuhan pengguna yang beragam, serta untuk mempromosikan pengetahuan, pembelajaran, pemahaman, dan apresiasi terhadap seni dan ilmu pengetahuan."
    
    
    <p>
    <h4>Perpustakaan STTB</h4>
        Perpustakaan Sekolah Tinggi Teknologi Bandung adalah sebuah lembaga atau fasilitas yang bertujuan untuk menyediakan akses ke berbagai sumber informasi, baik cetak maupun digital, yang relevan dengan kegiatan belajar-mengajar dan penelitian yang dilakukan oleh mahasiswa, dosen, dan staf STTB. Perpustakaan ini berperan penting dalam mendukung proses pembelajaran, penelitian, dan pengembangan ilmu pengetahuan di lingkungan kampus.

    <p>
        Perpustakaan Sekolah Tinggi Teknologi Bandung berupaya untuk terus mengembangkan koleksi dan layanan sesuai dengan perkembangan teknologi dan kebutuhan pengguna agar dapat memberikan kontribusi yang optimal dalam mendukung pencapaian tujuan pendidikan dan penelitian di kampus STTB.
    <p>
    <h4>Perpustakaan Menurut UU NO 43. THN. 2007</h4>
        Perpustakaan adalah institusi yang mengumpulkan pengetahuan tercetak dan terekam, mengelolanya dengan cara khusus guna memenuhi kebutuhan intelektualitas para penggunanya melalui berbagai cara interaksi pengetahuan.

